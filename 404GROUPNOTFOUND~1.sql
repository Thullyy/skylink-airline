/*REPORT: MOST USED ROUTE PER AIRCRAFT*/
CREATE OR REPLACE VIEW V_AIRCRAFT_ROUTE_USAGE AS
SELECT
    A.AIRCRAFT_ID,
    M.MODEL_ID,
    R.ROUTE_ID,
    R.ROUTE_CODE,
    (SELECT COUNT(F2.FLIGHT_ID)
     FROM FLIGHT F2
     WHERE F2.AIRCRAFT_ID = A.AIRCRAFT_ID
     AND   F2.ROUTE_ID    = R.ROUTE_ID) AS TIMES_FLOWN,
    (SELECT COUNT(BD.PASSENGER_ID)
     FROM BOOKING_DETAIL BD
     JOIN FLIGHT F3 ON BD.FLIGHT_ID = F3.FLIGHT_ID
     WHERE F3.AIRCRAFT_ID = A.AIRCRAFT_ID
     AND   F3.ROUTE_ID    = R.ROUTE_ID) AS TOTAL_PASSENGERS,
    (SELECT AP.AIRPORT_NAME
     FROM AIRPORT AP
     WHERE AP.AIRPORT_ID = R.DEPARTURE_AIRPORT_ID) AS DEPARTURE_AIRPORT,
    (SELECT AP.AIRPORT_NAME
     FROM AIRPORT AP
     WHERE AP.AIRPORT_ID = R.ARRIVAL_AIRPORT_ID) AS ARRIVAL_AIRPORT
FROM AIRCRAFT A
JOIN MODEL  M ON A.MODEL_ID  = M.MODEL_ID
JOIN ROUTE  R ON R.ROUTE_ID  IN (
    SELECT DISTINCT F5.ROUTE_ID
    FROM FLIGHT F5
    WHERE F5.AIRCRAFT_ID = A.AIRCRAFT_ID
)
ORDER BY A.AIRCRAFT_ID, TIMES_FLOWN DESC;


