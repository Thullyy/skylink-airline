       a.MODEL_ID,
       COUNT(f.FLIGHT_ID) AS TOTAL_FLIGHTS,
       COUNT(bd.PASSENGER_ID) AS TOTAL_PASSENGERS
FROM ROUTE r
JOIN FLIGHT f
ON r.ROUTE_ID = f.ROUTE_ID
JOIN AIRCRAFT a
ON f.AIRCRAFT_ID = a.AIRCRAFT_ID
JOIN BOOKING_DETAIL bd
ON f.FLIGHT_ID = bd.FLIGHT_ID
GROUP BY r.ROUTE_CODE,
         a.MODEL_ID
ORDER BY TOTAL_PASSENGERS DESC;

