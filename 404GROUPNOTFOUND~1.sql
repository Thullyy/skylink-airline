CREATE OR REPLACE VIEW vw_POPULAR_destinations AS
SELECT INITCAP(a.AIRPORT_NAME) AS DESTINATION,
       COUNT(b.BOOKING_ID) AS TOTAL_bookings,
       RANK() OVER (ORDER BY COUNT(b.BOOKING_ID) DESC) AS POPUALRITY_RANK
FROM BOOKING b
JOIN BOOKING_DETAIL bd ON b.BOOKING_ID = bd.BOOKING_ID
JOIN FLIGHT f          ON bd.Flight_ID = f.Flight_ID
JOIN ROUTE r           ON f.ROUTE_ID   = r.ROUTE_ID
JOIN AIRPORT a         ON r.ARRIVAL_AIRPORT_ID = a.Airport_ID
GROUP BY a.AIRPORT_NAME, INITCAP(a.AIRPORT_NAME);

